SELECT
    "EMPRESA_SERVICIO" AS EMPRESA,
    "ANIO" AS ANIO, 
    "ANIO-MES" AS ANIO_MES,
    "ANIO-MES-DIA" AS FECHA, 
    SUM("OCUPACION_TRAMO") AS OCUPACION,
    ( SUM("INGRESOS") / SUM("NKILOMETROSACUMULADOSRUTAS") ) AS FIK,
    ( SUM("KILOMETROPUNTOxOCUPACION") / SUM("KILOMETROPUNTOxCAPACIDADASIENTO") ) AS FOP,
    SUM("CUENTACORRIDA") AS CORRIDAS
FROM DATA
WHERE 
    ("ANIO" >= '2023') 
GROUP BY 
    "ANIO", 
    "ANIO-MES",
    "ANIO-MES-DIA"
ORDER BY FECHA